const KNOWN = Object.keys(MESSAGES);

// "zh-CN" → zhCN, "zh-Hant-TW" → zhTW, "pt-BR" → pt, "en-US" → en …
function detectLocale(systemLocale) {
  const lower = String(systemLocale || "").toLowerCase();
  if (lower.startsWith("zh")) {
    return /tw|hk|mo|hant/.test(lower) ? "zhTW" : "zhCN";
  }
  const prefix = lower.split("-")[0];
  return KNOWN.find((key) => key.toLowerCase() === prefix) || "en";
}

function normalizeLocale(locale) {
  return KNOWN.includes(locale) ? locale : null;
}

function translate(locale, key, params) {
  let message = (MESSAGES[locale] || MESSAGES.en)[key] || MESSAGES.en[key] || key;
  for (const [name, value] of Object.entries(params || {})) {
    message = message.replaceAll(`{${name}}`, String(value));
  }
  return message;
}

module.exports = { detectLocale, normalizeLocale, translate };
